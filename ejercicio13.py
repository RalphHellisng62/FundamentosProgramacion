'''
Ejercicio 13:
Realiza un algoritmo que lea un numero y que muestre su raiz cuagrada y su raiz cubica

entradas: 
num: int

salidas
raiz cuadrada
raiz cubica
'''
num= float(input("Pida el numero: "))
Rcuadrada= num**(1/2)
Rcubica= num**(1/3)
print("La raiz cuadrada del numero es: ", Rcuadrada)
print("La raiz cubica del numero es: ", Rcubica)